// pid to ProcId
int pidToProcid(int pid){

    acquire(&ptable.lock);
    int loop = 0;
    for(loop =0;loop < NPROC;loop++){
      if(ptable.proc[loop].pid == pid)
        break;
    }
    release(&ptable.lock);
    if(loop==NPROC)
      return -1;
    else
      return loop;
}

